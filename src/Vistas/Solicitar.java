  private void enviardatos(){
      
      try{
      //conectarse a la Bd
      Connection con= SingletonConexion.conectarse();
      
      //obtener datos a insertar
      String observaciones = observacionesbox.getText();//1
      String fecha = Datefield.getText();//2
      
      String selectedtipo = (String)Tipobox.getSelectedItem();
       Statement st1= con.createStatement();
       ResultSet rs1= st1.executeQuery("Select IdTipoMovimiento from TipoMovimiento where Nombre='"+selectedtipo+"'");
       int tipo = rs1.getInt("IdTipoMovimiento"); //3   
       
       String selectedmateria = (String)Materiabox.getSelectedItem();
       Statement st2= con.createStatement();
       ResultSet rs2= st2.executeQuery("Select IdMateria from Materia where Nombre='"+selectedmateria+"'");
       int materia = rs2.getInt("IdMateria");//4
       
       int matricula= Integer.parseInt (InicioDeSesion.getmatricula());//5
       
       String selectedgrupo = (String)Grupobox.getSelectedItem();
      int grupoid = Integer.parseInt(selectedgrupo);//6
      
      String selectedprograma = (String)programaeducativo.getSelectedItem();
       Statement st3= con.createStatement();
       ResultSet rs3= st3.executeQuery("Select IdProgramaEducativo from ProgramaEducativo where Nombre='"+selectedprograma+"'");
       int programa = rs3.getInt("IdProgramaEducativo");//7
      
      int estatus = 1;//8
      
      String sql= "INSERT INTO Movimiento (Observaciones,Fecha,IdTipoMovimiento,IdMateria,IdAlumno,IdGrupo,IdProgramaeducativo,IdEstatus) values ('"+observaciones+"','"+fecha+"','"+tipo+"','"+materia+"','"+matricula+"','"+grupoid+"','"+programa+"','"+estatus+"')" ;
      Statement st = con.createStatement();
      st.executeUpdate(sql);
      
        Solicitar_exito form= new Solicitar_exito();/*opcion factory*/
        form.setVisible(true);
        this.dispose();
          System.out.println("Conectado correctamente");
       
       }catch(Exception e){
        Solicitar_fracaso form= new Solicitar_fracaso ();/*opcion factory*/
        form.setVisible(true);
        this.dispose();
       }
      
  }
    
   
 
    

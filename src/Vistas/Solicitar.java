  private void llenarcombo1(){ 
        Connection con= SingletonConexion.conectarse();
        String matricula = InicioDeSesion.getmatricula();
        programaeducativo.removeAllItems();
        programaeducativo.addItem(null);
        try{ 
           String sql= "SELECT Nombre FROM ProgramaEducativo p JOIN Alumno_ProgramaEducativo A ON p.idProgramaEducativo = A.idProgramaEducativo where IdAlumno ="+matricula+";";
           Statement st= con.createStatement();
           ResultSet rs = st.executeQuery(sql);
        while(rs.next()){ 
            String name = rs.getString("Nombre");
           
           programaeducativo.addItem(name);
            
        }         
        }catch(Exception e){ 
            System.out.println("Error");
        }
        
    }
